    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes() {
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        javax.swing.DefaultComboBoxModel comboBoxModel = new javax.swing.DefaultComboBoxModel(courses.toArray());
        courseSelectComboBox.setModel(comboBoxModel);
        courseSelectComboBox.removeAllItems();

        for (CourseEntry course : courses) {
            courseSelectComboBox.addItem(course.getCourseCode());
        }
    }

    
    public void rebuildStudentComboBoxes() {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();

        javax.swing.DefaultComboBoxModel studentComboBoxModel = new javax.swing.DefaultComboBoxModel(students.toArray());
        studentSelectComboBox.setModel(studentComboBoxModel);

        javax.swing.DefaultComboBoxModel scheduleComboBoxModel = new javax.swing.DefaultComboBoxModel(students.toArray());
        selectStudentForScheduleComboBox.setModel(scheduleComboBoxModel);
    }





