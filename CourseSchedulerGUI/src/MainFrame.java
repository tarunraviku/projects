    private void scheduleCoursesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesSubmitButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(studentSelectComboBox.getSelectedIndex()).getStudentID();
        String courseCode=courseSelectComboBox.getSelectedItem().toString();
        String semester=currentSemesterComboBox.getSelectedItem().toString();
        String status="";
        
        if(ScheduleQueries.getScheduledStudentCount(semester, courseCode) >= CourseQueries.getCourseSeats(semester, courseCode))
        {
            status="W";
        }
        else
        {
            status="S";
        }
        
        
        ScheduleEntry scheduleObject= new ScheduleEntry(semester, courseCode, studentID, status);
        if (status=="W")
        {
            ScheduleCoursesStatusLabel.setText("Course " + courseCode + " has been waitlisted for " + students.get(studentSelectComboBox.getSelectedIndex()).getLastName()+", "+students.get(studentSelectComboBox.getSelectedIndex()).getFirstName());

        }
        else if (status=="S")
        {
            ScheduleCoursesStatusLabel.setText("Course " + courseCode + " has been scheduled for " + students.get(studentSelectComboBox.getSelectedIndex()).getLastName()+", "+students.get(studentSelectComboBox.getSelectedIndex()).getFirstName());

        }
        
        ScheduleQueries.addScheduleEntry(scheduleObject);      
    }//GEN-LAST:event_scheduleCoursesSubmitButtonActionPerformed

